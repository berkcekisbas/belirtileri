                <h4>Lenfoma Riskini Artıran Faktörler</h4>
                Bazı faktörler lenfoma riskini artırabilir. Bu faktörlerden bazıları (örneğin sigara kullanmak gibi)
                ortadan kaldırılabilirken kalıtımsal özellikler (yakın aile bireylerinde görülen kanser vakaları gibi)
                için yapacak bir şey bulunmamaktadır. Ancak riski bilip buna göre tedbir almak, düzenli olarak kontrol
                yaptırmak kanser tedavisinde son derece önemli olan erken teşhis noktasında avantaj sağlayabilir.<p></p>

                <p>Lenfoma riskini artıran faktörler şu şekilde sıralanmaktadır;</p>

                <p><strong>Yaş</strong>: Yaş alma diğer kanser türlerinin çoğunda olduğu gibi lenfoma riskini de
                    artırmaktadır. Lenfoma bazen genç sayılabilecek yaş gruplarında da görülen bir kanser türü olmasına
                    karşın en çok 60 yaş üzeri yetişkinlerde görülür.</p>

                <p><strong>Cinsiyet</strong>: Hodgkin olmayan lenfoma (NHL) erkeklerde daha çok görülmektedir. Ancak
                    NHL’nin bazı türleri kadınlar arasında daha yaygındır. Bunun nedeni, yani neden bazı lenfoma
                    türlerinin bir cinsiyette daha çok görüldüğü konusunda net bir bilgi bulunmamaktadır.</p>

                <p><strong>Etnik Köken</strong>: ABD’de yapılan araştırmalar lenfomanın beyazlar arasında daha yaygın
                    olduğunu ortaya koymaktadır. Dünya genelinde ise lenfoma gelişmiş ülkelerde daha çok görülen bir
                    kanser türüdür.</p>

                <p><strong>Zararlı Kimyasallar</strong>: Çalışma ortamı ve koşullarına gereği çeşitli kimyasal maddelere
                    (benzin, tarım ilaçları, temizlik malzemeleri…) maruz kalan insanlarda lenfoma görülme riski
                    artmaktadır. Buna ek olarak diğer bir kanser türünü tedavi etmek için kullanılan kemoterapi ilaçları
                    uzun dönemde non-Hodgkin lenfomaya yol açabilmektedir.</p>

                <p><strong>Bağışıklık Sistemi</strong>: Herhangi bir nedenden ötürü (organ nakli, HIV…) zayıflayan
                    bağışıklık sistemi lenfoma oluşma riskini artırır. Eğer bağışıklık sistemi doğuştan zayıfsa lenfoma
                    çocukluk döneminde ortaya çıkabilir.</p>

                <p>Romatoid artrit, sistemik lupus eritematozus (SLE veya lupus), Sjögrens hastalığı, Çölyak (glütene
                    duyarlı enteropati) ve diğer bağışıklık sistemi hastalıkları lenfoma riskini artıran faktörler
                    arasındadır.</p>

                <p><strong>Beslenme Alışkanlıkları</strong>: Bazı bilimsel araştırmalar aşırı kilolum olmanın ve yüksek
                    oranda yağ ve kırmızı et içeren beslenme şeklinin lenfoma riskini artırdığını ortaya koymaktadır.
                    Ancak lenfoma nispeten sağlıklı beslenen insanlarda da görülebildiği için bu konuda daha fazla
                    bilimsel araştırmaya ihtiyaç duyulmaktadır.</p>
